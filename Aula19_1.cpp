#include <iostream>

int main() {
	int a, b, c, d, e, f, g, h, i, x, y, CPF;
	
	scanf("%d", &CPF);
	a = CPF / 100000000;
	b = (CPF / 10000000) - (a * 10);
	c = (CPF / 1000000) - ((a * 100) + (b * 10));
	d = (CPF / 100000) - ((a * 1000) + (b * 100) + (c * 10));
	e = (CPF / 10000) - ((a * 10000) + (b * 1000) + (c * 100) + (d * 10));
	f = (CPF / 1000) - ((a * 100000) + (b * 10000) + (c * 1000) + (d * 100) + (e * 10));
	g = (CPF / 100) - ((a * 1000000) + (b * 100000) + (c * 10000) + (d * 1000) + (e * 100) + (f * 10));
	h = (CPF / 10) - ((a * 10000000) + (b * 1000000) + (c * 100000) + (d * 10000) + (e * 1000) + (f * 100) + (g * 10));
	i = (CPF / 1) - ((a * 100000000) + (b * 10000000) + (c * 1000000) + (d * 100000) + (e * 10000) + (f * 1000) + (g * 100) + (h * 10));

	x = ((a * 1) + (b * 2) + (c * 3) + (d * 4) + (e * 5) + (f * 6) + (g * 7) + (h * 8) + (i * 9)) % 11;
	if (x == 10) {
		x = 0;
	}
	
	y = ((a * 0) + (b * 1) + (c * 2) + (d * 3) + (e * 4) + (f * 5) + (g * 6) + (h * 7) + (i * 8) + (x * 9)) % 11;	

	if (y == 10) {
		y = 0;
	}
	
	printf("CPF + DV = %d%d%d.%d%d%d.%d%d%d-%d%d", a, b, c, d, e, f, g, h, i, x, y);
}
